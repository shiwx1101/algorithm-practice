/**
 * 找出第k大的元素
 *
 * @author shiwenxiang
 * @version : FindKItem.java, v0.1 2020/9/9 shiwenxiang Exp $$
 */
public class FindKItem {

    int[] a = {};


    public int find(int[] a, int k) {
        int length = a.length;
        if (k < 1 || k > length) {
            throw new IllegalArgumentException();
        }


        return 0;
    }
}
